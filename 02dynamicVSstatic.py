# Statically VS Dynamically Typing

# A programming language is stacially typed if the type of a variable
# is known at compile time, so before running the script. C, C++ or Java are statically typed languages
# Python is dynamically typed and it means that the type is associated
# with run-time values, and not with named variables. Values stored
# in variables have type and not the name or variable itself
# We can see what type of data a variable stores using the built-it type()
# function.
# The built-in id() function returns the memory address where the values
# referenced by the variable is stored. Or simply said, it returns the address
# of the variable in memory

